let currentLang = document.documentElement.lang === "es" ? "es" : "en";

function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (resources[currentLang][key] !== undefined) {
     if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = resources[currentLang][key];
      } else {
        el.textContent = resources[currentLang][key];
      }
    }
  });


  const langBtn = document.getElementById("lang-toggle");
  if (langBtn) langBtn.textContent = currentLang === "es" ? "ES" : "EN";

  document.documentElement.lang = currentLang;
}

function toggleLanguage() {
  currentLang = currentLang === "es" ? "en" : "es";
  applyTranslations();
}

document.addEventListener("DOMContentLoaded", applyTranslations);